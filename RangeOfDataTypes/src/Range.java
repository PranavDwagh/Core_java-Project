
public class Range {

	public static void main(String[] args) {

		byte n = (byte) 129;
		
		System.out.println(n);
		
		
		
		/// following is the way of find out range of primitive data type
		// methods starts with Capital letters  are the wrapper classes
		
		System.out.println("Short " +Short.MIN_VALUE + " to " + Short.MAX_VALUE) ;
		System.out.println("Long "+Long.MIN_VALUE + " to " + Long.MAX_VALUE) ;
		
		System.out.println("Integer " +Integer.MIN_VALUE + " to " + Integer.MAX_VALUE) ;
		
		System.out.println(" Character " +Character.MIN_VALUE + " to " + Character.MAX_VALUE) ;
		
		System.out.println("Float "+Float.MIN_VALUE + " to " + Float.MAX_VALUE) ;
		
		System.out.println("Double "+Double.MIN_VALUE + " to " + Double.MAX_VALUE) ;
	}

}
